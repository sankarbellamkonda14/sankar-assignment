public class RelationalOperation
{
 public static void main(String[]args)
  {
   int a =8;
   int b =5;
   System.out.println(" comparison value of a==b "+ (a==b));  //falsee
   System.out.println(" comparison value of a!=b "+ (a!=b));  //True
   System.out.println(" comparison value of a<b "+ (a<b));    //false
   System.out.println(" comparison value of a>b "+ (a>b));    //True
   System.out.println(" comparison value of a>=b "+ (a>=b));  //True
   System.out.println(" comparison value of a<=b "+ (a<=b));  //False
  }
}





   