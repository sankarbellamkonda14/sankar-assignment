public class BitwiseOperator
{
 public static void main(String[]args)
 {
  int a=10;
  int b=8;
  
  System.out.println(" The result of bitwise AND ="+(a&b));
  System.out.println(" The result of bitwise OR ="+(a|b));
  System.out.println(" The result of bitwise XOR ="+(a^b));
  System.out.println(" The result of uniry bitwise NOT of a ="+(~a));
  System.out.println(" The result of uniry bitwise NOT of b ="+(~b));
  System.out.println(" The result of bitwise left shift ="+(a<<b));
  System.out.println(" The result of bitwise right shift ="+(a>>b));
 }
}
  